    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txt_ruta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "De una ruta", "Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            String ruta = txt_ruta.getText().trim();
            try {
                principal p = new principal();
                txt_texto.append(p.leer(ruta));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ocurrio un error\n" + e, "Error!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txt_ruta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "De una ruta", "Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            String ruta = txt_ruta.getText().trim();
            String nuevoS = txt_texto.getText();
            try {
                principal p = new principal();
                p.modificar(ruta, nuevoS);
                JOptionPane.showMessageDialog(null, "bien ");
                limpiar();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ocurrio un error\n" + e, "Error!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

